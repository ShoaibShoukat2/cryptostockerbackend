database_url = os.getenv('DATABASE_URL', '')
if database_url.startswith('postgres'):
    import re
    match = re.match(
        r'postgres://([^:]+):([^@]+)@([^:]+):(\d+)/(.+)',
        database_url,
    )
    if match:
        user, password, host, port, name = match.groups()
        DATABASES['default'] = {
            'ENGINE': 'django.db.backends.postgresql',
            'NAME': name,
            'USER': user,
            'PASSWORD': password,
            'HOST': host,
            'PORT': port,
        }

